namespace MeshWeaver.Layout;

/// <summary>
/// Provides constants for different modal areas.
/// </summary>
public static class ModalArea
{
    /// <summary>
    /// Represents the header area of the modal.
    /// </summary>
    public const string Header = nameof(Header);

    /// <summary>
    /// Represents the main content area of the modal.
    /// </summary>
    public const string Main = nameof(Main);

    /// <summary>
    /// Represents the footer area of the modal.
    /// </summary>
    public const string Footer = nameof(Footer);
}
